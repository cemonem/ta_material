char announcement[14] = "Stay safe :)\n";

/**********************************************************************
 * ------------------------------ TASK0 -------------------------------
 *
 * Important announcement every 5secs :)
 *
 **********************************************************************/
TASK(TASK0)
{
	SetRelAlarm(ALARM_TSK0, 100, 5000);
	while(1) {
        WaitEvent(ALARM_EVENT);
        ClearEvent(ALARM_EVENT);
        transmitBuffer_push(announcement,13);
	}
	TerminateTask();
}


